
public class jlearn{

    static void cars(){
        String[][] day = {{"monday", "tuesday","wednesday"}, {"thursday","friday","saturday"}};
        
        
        for(int i=0; i<=day.length; i++ ){
            for (int j=0; j<=day.length; j++){
                System.out.println(day[i][j]);
            }
        
        }
    }
    public static void main(String[] args){
        
        cars();
    }

    
}

